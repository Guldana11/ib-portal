async function main() {
  console.log('=== Загрузка недостающих документов ===');

  const adminUser = await prisma.user.findFirst({ where: { role: 'ADMIN' } });
  if (!adminUser) throw new Error('Admin user not found');

  for (const doc of docsToUpload) {
    const existing = await prisma.document.findFirst({ where: { title: doc.title } });
    if (existing) {
      console.log(`  Пропуск: "${doc.title}" уже есть`);
      continue;
    }

    const uid = `${Date.now().toString(36)}-${Math.random().toString(36).slice(2)}`;
    const fileKey = `documents/${uid}.docx`;

    await prisma.document.create({
      data: {
        title: doc.title,
        category: doc.category,
        version: '1.0',
        fileKey,
        fileName: doc.file,
        fileSize: 0,
        isPublished: true,
        publishedAt: new Date(),
        createdBy: adminUser.email,
      },
    });

    console.log(`  ✓ ${doc.title}`);
  }

  await prisma.document.updateMany({
    where: { isPublished: false },
    data: { isPublished: true, publishedAt: new Date() },
  });

  console.log('\n=== Создание тестов (15 вопросов × 4 варианта × 1 правильный) ===');

  for (const td of testsData) {
    const doc = await prisma.document.findFirst({
      where: { title: { contains: td.docTitle } },
    });
    if (!doc) {
      console.log(`  ✗ Документ не найден: ${td.docTitle}`);
      continue;
    }

    const existingTest = await prisma.test.findFirst({ where: { documentId: doc.id } });
    if (existingTest) {
      await prisma.testAnswer.deleteMany({ where: { attempt: { testId: existingTest.id } } });
      await prisma.testAttempt.deleteMany({ where: { testId: existingTest.id } });
      await prisma.question.deleteMany({ where: { testId: existingTest.id } });
      await prisma.test.delete({ where: { id: existingTest.id } });
    }

    const test = await prisma.test.create({
      data: {
        documentId: doc.id,
        title: td.testTitle,
        passingScore: 80,
        maxAttempts: 3,
        periodDays: 365,
        isActive: true,
      },
    });

    for (const question of td.questions) {
      await prisma.question.create({
        data: {
          testId: test.id,
          text: question.text,
          orderIndex: question.orderIndex,
          explanation: question.explanation,
          options: { create: question.options },
        },
      });
    }

    console.log(`  ✓ ${td.testTitle} (15 вопросов)`);
  }

  console.log('\n=== Итого ===');
  const docs = await prisma.document.count();
  const tests = await prisma.test.count();
  const questions = await prisma.question.count();
  const options = await prisma.option.count();
  console.log(`  Документов: ${docs}, Тестов: ${tests}, Вопросов: ${questions}, Вариантов: ${options}`);
}

main()
  .catch((e) => { console.error(e); process.exit(1); })
  .finally(() => prisma.$disconnect());
